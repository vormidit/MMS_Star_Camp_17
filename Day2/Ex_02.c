#include<stdio.h>

int main(int argc, char* argv[]){


    int binaryNumber  = 0b1011;//11 in decimal
    int octalNumber  = 031;// 25 in decimal
    int hexNumber  = 0x1A;//26 in decimal

    printf("binaryNumber: %d\n", binaryNumber);
    printf("octalNumber: %d\n", octalNumber);
    printf("hexNumber: %d\n", hexNumber);

    return 0;
}